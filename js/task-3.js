const ADMIN_PASSWORD = 'admin';

let message = prompt('task-3\n\Введіть пароль адміністратора');
switch (message) {
    case null:
        message = 'Скасовано користувачем!';
        break;
    case ADMIN_PASSWORD:
        message = 'Ласкаво просимо!';
        break;
    default:
        message = "Доступ заборонений, невірний пароль!";
        break;
}

alert(message);